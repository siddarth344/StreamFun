package com.example.streamfun;

public class Member {
    private String myVideoName;
    private String myVideoUrl;

    public Member() {
    }

    public String getMyVideoName() {
        return myVideoName;
    }

    public void setMyVideoName(String myVideoName) {
        this.myVideoName = myVideoName;
    }

    public String getMyVideoUrl() {
        return myVideoUrl;
    }

    public void setMyVideoUrl(String myVideoUrl) {
        this.myVideoUrl = myVideoUrl;
    }
}
